"""
knowledge_fusion_rules.py
所有 Agent 共用的三层知识融合规则模板。

设计原则：
1. 第一层（Ksher 产品知识库）：涉及产品细节 → 必须基于知识库回答
2. 第二层（模型行业知识）：涉及行业背景/市场数据 → 用模型知识补充
3. 第三层（融合推理）：自然融合前两层 → 生成连贯专业的回答
4. 冲突规则：知识库 > 模型自身知识（内部数据以知识库为准）
5. 标注规范：区分数据来源，增强可信度
"""

